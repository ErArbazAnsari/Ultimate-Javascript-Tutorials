// Loops in Javascript
// for, while, do-while

console.log("\nFor loop in JavaScript");
// for loop in javascript
for (let i = 1; i <= 10; i++) {
  console.log("Arbaz Ansari", i);
}

console.log("\n\nWhile loop in JavaScript");
// while loop
let i = 1;
while (i <= 10) {
  console.log("Arbaz Ansari", i);
  i++;
}
